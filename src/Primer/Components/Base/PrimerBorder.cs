namespace Primer.Components
{
    /// <summary>
    /// Primer border
    /// </summary>
    public enum PrimerBorder
    {
        /// <summary>
        /// Use the components border
        /// </summary>
        Component,

        /// <summary>
        /// No border
        /// </summary>
        None,

        /// <summary>
        /// Default border
        /// </summary>
        Default,

        /// <summary>
        /// Responsive border thats added at the small breakpoint
        /// </summary>
        ResponsiveAddAtSmallBreakpoint,

        /// <summary>
        /// Responsive border thats added at the medium breakpoint
        /// </summary>
        ResponsiveAddAtMediumBreakpoint,

        /// <summary>
        /// Responsive border thats added at the large breakpoint
        /// </summary>
        ResponsiveAddAtLargeBreakpoint,

        /// <summary>
        /// Responsive border thats added at the extra large breakpoint
        /// </summary>
        ResponsiveAddAtExtraLargeBreakpoint,

        /// <summary>
        /// Responsive border thats removed at the small breakpoint
        /// </summary>
        ResponsiveRemoveAtSmallBreakpoint,

        /// <summary>
        /// Responsive border thats removed at the medium breakpoint
        /// </summary>
        ResponsiveRemoveAtMediumBreakpoint,

        /// <summary>
        /// Responsive border thats removed at the large breakpoint
        /// </summary>
        ResponsiveRemoveAtLargeBreakpoint,

        /// <summary>
        /// Responsive border thats removed at the extra large breakpoint
        /// </summary>
        ResponsiveRemoveAtExtraLargeBreakpoint,
    }
}
